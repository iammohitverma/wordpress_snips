document.getElementById("findRTO").addEventListener("click", function(e) {
    e.preventDefault;
    document.querySelector(".message").style.display = "none";
    const user_input = document.getElementById("rtoNumber").value;
    if (user_input != '') {
        let srchVal = user_input.toUpperCase();
        let state_code = user_input.split("-");
        state_code = state_code[0].toUpperCase();

        for (let i = 0; i < rto_list.length; i++) {
            let stateList = rto_list[i].RTO;
            if (state_code == rto_list[i].Code) {
                stateList.forEach((item) => {
                    if (item[srchVal] != undefined) {
                        document.getElementById("error").style.display = "none";
                        document.querySelector(".message").style.display = "block";
                        document.getElementById("setRTO").innerHTML = item[srchVal];
                        document.getElementById("setState").innerHTML = rto_list[i].State;
                    } else {
                        document.getElementById("error").style.display = "block";
                        document.getElementById("error").innerHTML = "Plese input correct RTO Details & search again";
                    }
                });
                break;
            } else {
                document.getElementById("error").style.display = "block";
                document.getElementById("error").innerHTML = "Plese input correct RTO Details & search again";
            }
        };
    } else {
        document.getElementById("error").style.display = "block";
        document.getElementById("error").innerHTML = "Please fill something";
    }
})